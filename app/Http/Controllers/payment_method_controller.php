    {
        $add=new payment_method();
        $add->credit_card_no=$request->input('credit_card_no');    
        $add->card_type =$request->input('card_type');  
        $add->expiry =$request->input('expiry');
        $add->cvv =$request->input('cvv');  
        $created=$add->save();
        if($created){

            return redirect()->back();
        }
    }

